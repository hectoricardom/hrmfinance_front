// Get signature file with metadata
export const getSignatureFile = async (request: SignatureRequest): Promise<SignatureFile | null> => {
  if (!request.signatureUrl) {
    return null;
  }

  // If it's a base64 data URL, return it as-is
  if (request.signatureUrl.startsWith('data:')) {
    return {
      url: request.signatureUrl,
      base64: request.signatureUrl,
      storageMethod: 'base64'
    };
  }

  // If it's a URL, return it
  return {
    url: request.signatureUrl,
    storageMethod: 'storage'
  };
};

// Get multiple signature files with metadata
export const getSignatureFiles = async (requests: SignatureRequest[]): Promise<(SignatureFile | null)[]> => {
  const promises = requests.map(request => getSignatureFile(request));
  return Promise.all(promises);
};

// Export signed requests to main database and mark as exported
export const exportSignedRequests = async (requestIds: string[]): Promise<{
  success: string[];
  errors: { id: string; error: string }[];
}> => {
  const results = {
    success: [] as string[],
    errors: [] as { id: string; error: string }[]
  };

  for (const requestId of requestIds) {
    try {
      const request = await getSignatureRequest(requestId);
      if (!request) {
        results.errors.push({ id: requestId, error: 'Request not found' });
        continue;
      }

      if (request.status !== 'signed') {
        results.errors.push({ id: requestId, error: 'Request is not signed' });
        continue;
      }

      const now = Date.now();

      // Create exported record
      const exportBody = {
        query: "addExportedSignature",
        params: {
          businessId: authStore.getBusinessId()
        },
        form: {
          ...request,
          exportedAt: now,
          originalRequestId: requestId
        }
      };

      await fetchGraphQLSS(exportBody);

      // Mark original request as exported
      await updateSignatureRequestStatus(requestId, 'exported', { exportedAt: now });

      results.success.push(requestId);
    } catch (error: any) {
      results.errors.push({
        id: requestId,
        error: error.message || 'Unknown error during export'
      });
    }
  }

  return results;
};

// Clean up exported requests
export const cleanupExportedRequests = async (requestIds: string[]): Promise<{
  success: string[];
  errors: { id: string; error: string }[];
}> => {
  const results = {
    success: [] as string[],
    errors: [] as { id: string; error: string }[]
  };

  for (const requestId of requestIds) {
    try {
      const request = await getSignatureRequest(requestId);
      if (!request) {
        results.errors.push({ id: requestId, error: 'Request not found' });
        continue;
      }

      if (request.status !== 'exported') {
        results.errors.push({ id: requestId, error: 'Request is not exported yet' });
        continue;
      }

      await deleteSignatureRequest(requestId);
      results.success.push(requestId);
    } catch (error: any) {
      results.errors.push({
        id: requestId,
        error: error.message || 'Unknown error during cleanup'
      });
    }
  }

  return results;
};

