// Get client signatures
export const getClientSignatures = async (clientId: string): Promise<any[]> => {
  try {
    const body = {
      query: "getClientSignatures",
      params: {
        businessId: authStore.getBusinessId(),
        clientId
      }
    };

    const response = await fetchGraphQLSS(body);
    // Handle nested response structure: response.data.data or response.data
    const signatures = response?.data?.data || response?.data || [];

    // Sort by signedAt descending
    return signatures.sort((a: any, b: any) => {
      const aTime = a.signedAt || 0;
      const bTime = b.signedAt || 0;
      return bTime - aTime;
    });
  } catch (error) {
    console.error('Error getting client signatures:', error);
    return [];
  }
};

// Cancel a signature request
export const cancelSignatureRequest = async (requestId: string): Promise<void> => {
  try {
    const currentUser = authStore.state.user;
    if (!currentUser) {
      throw new Error('Usuario no autenticado');
    }

    const request = await getSignatureRequest(requestId);
    if (!request) {
      throw new Error('Solicitud no encontrada');
    }

    if (request.requestedBy !== currentUser.uid && !authStore.isAdmin()) {
      throw new Error('No autorizado para cancelar esta solicitud');
    }

    if (request.status !== 'pending') {
      throw new Error('Solo se pueden cancelar solicitudes pendientes');
    }

    await updateSignatureRequestStatus(requestId, 'cancelled');
  } catch (error) {
    console.error('Error cancelling signature request:', error);
    throw error;
  }
};

// Delete signature request
export const deleteSignatureRequest = async (requestId: string): Promise<void> => {
  try {
    const currentUser = authStore.state.user;
    if (!currentUser) {
      throw new Error('Usuario no autenticado');
    }

    const request = await getSignatureRequest(requestId);
    if (!request) {
      throw new Error('Solicitud no encontrada');
    }

    // Only admin or the creator can delete
    if (!authStore.isAdmin() && request.requestedBy !== currentUser.uid) {
      throw new Error('No autorizado para eliminar esta solicitud');
    }

    const body = {
      query: "deleteSignatureRequest",
      params: {
        businessId: authStore.getBusinessId(),
        id: requestId
      }
    };

    await fetchGraphQLSS(body);

    // If the request was signed, also delete from clientSignatures
    if (request.status === 'signed' && request.clientId) {
      try {
        const deleteClientSigBody = {
          query: "deleteClientSignature",
          params: {
            businessId: authStore.getBusinessId(),
            id: `${request.clientId}_${requestId}`
          }
        };
        await fetchGraphQLSS(deleteClientSigBody);
      } catch (error) {
        console.warn('Could not delete from clientSignatures:', error);
      }
    }

    devLog(`Successfully deleted signature request: ${requestId}`);
  } catch (error) {
    console.error('Error deleting signature request:', error);
    throw error;
  }
};

// Generate shareable link using accessToken (for public access without login)
export const generateSignatureLink = (accessToken: string, id: string): string => {
  const baseUrl = window.location.origin;
  return `${baseUrl}/#/sign/${id}/${accessToken}`;
};

// Generate QR code data URL for the signature link
export const generateSignatureQR = async (accessToken: string, id: string): Promise<string> => {
  const link = generateSignatureLink(accessToken, id);
  return `https://api.qrserver.com/v1/create-qr-code/?size=200x200&data=${encodeURIComponent(link)}`;
};

