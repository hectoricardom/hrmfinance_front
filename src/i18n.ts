const getInitialLanguage = (): Language => {
  if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('hrmfinance-language') as Language;
    if (saved && ['en', 'es'].includes(saved)) {
      return saved;
    }
  }
  return 'en';
};

// Global signal for the current language
const [currentLanguage, setCurrentLanguage] = createSignal<Language>(getInitialLanguage());

export const useTranslation = () => {
  const language = () => currentLanguage();
  
  const setLanguage = (lang: Language) => {
    setCurrentLanguage(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem('hrmfinance-language', lang);
    }
  };
  
  const t = (key: string, fallback?: string, interpolation?: Record<string, string | number>): string => {
    const lang = currentLanguage(); // This tracks the signal properly
    const keys = key.split('.');
    let current: any = translations[lang];
    
    for (const k of keys) {
      if (current && typeof current === 'object' && k in current) {
        current = current[k];
      } else {
        return fallback || key;
      }
    }
    
    let translatedText = typeof current === 'string' ? current : fallback || key;
    
    // Handle interpolation if provided
    if (interpolation && typeof translatedText === 'string') {
      Object.keys(interpolation).forEach(interpolationKey => {
        const value = interpolation[interpolationKey];
        const placeholder = `{{${interpolationKey}}}`;
        translatedText = translatedText.replace(new RegExp(placeholder, 'g'), String(value));
      });
    }
    
    return translatedText;
  };
  
  return { t, language, setLanguage, currentLanguage };
};